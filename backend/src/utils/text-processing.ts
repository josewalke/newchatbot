/**
 * Limpia el texto de respuestas del chatbot para evitar duplicaciones y mejorar la legibilidad
 */
export function cleanChatbotResponse(text: string): string {
  return text
    // Eliminar confirmaciones duplicadas
    .replace(/(\s*¬øDeseas confirmar\?\s*){2,}/gi, " ¬øDeseas confirmar?")
    .replace(/(\s*¬øTe gustar√≠a confirmar\?\s*){2,}/gi, " ¬øTe gustar√≠a confirmar?")
    .replace(/(\s*¬øQuieres confirmar\?\s*){2,}/gi, " ¬øQuieres confirmar?")
    
    // Eliminar espacios m√∫ltiples
    .replace(/\s{2,}/g, " ")
    
    // Limpiar saltos de l√≠nea m√∫ltiples
    .replace(/\n{3,}/g, "\n\n")
    
    // Limpiar puntos m√∫ltiples
    .replace(/\.{3,}/g, "...")
    
    // Limpiar signos de exclamaci√≥n m√∫ltiples
    .replace(/!{2,}/g, "!")
    
    // Limpiar signos de interrogaci√≥n m√∫ltiples
    .replace(/\?{2,}/g, "?")
    
    // Limpiar espacios al inicio y final
    .trim();
}


/**
 * Normaliza el texto del usuario para mejorar la comprensi√≥n
 */
export function normalizeUserText(text: string): string {
  return text
    // Normalizar abreviaciones comunes
    .replace(/\bq\b/gi, 'que')
    .replace(/\bxq\b/gi, 'porque')
    .replace(/\bpa\b/gi, 'para')
    .replace(/\bta\b/gi, 'est√°')
    .replace(/\bke\b/gi, 'que')
    .replace(/\baki\b/gi, 'aqu√≠')
    .replace(/\bq tal\b/gi, 'qu√© tal')
    .replace(/\bq hora\b/gi, 'qu√© hora')
    .replace(/\bq dia\b/gi, 'qu√© d√≠a')
    
    // Limpiar muletillas y sonidos
    .replace(/\s+(eee+|mmm+|aj√°+|eh+|um+)\s+/gi, ' ')
    .replace(/\s+(bueno+|pues+|entonces+)\s+/gi, ' ')
    
    // Normalizar puntuaci√≥n
    .replace(/\s*\.{2,}/g, '.')
    .replace(/\s*!{2,}/g, '!')
    .replace(/\s*\?{2,}/g, '?')
    
    // Limpiar espacios m√∫ltiples
    .replace(/\s{2,}/g, ' ')
    .trim();
}

